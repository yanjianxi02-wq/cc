  end if;
  if not exists (
    select 1 from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public' and tablename = 'selection_items'
  ) then
    alter publication supabase_realtime add table public.selection_items;
  end if;
  if not exists (
    select 1 from pg_publication_tables
    where pubname = 'supabase_realtime'
      and schemaname = 'public' and tablename = 'product_overrides'
  ) then
    alter publication supabase_realtime add table public.product_overrides;
  end if;
end $$;
