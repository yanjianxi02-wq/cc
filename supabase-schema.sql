create extension if not exists pgcrypto;

create table if not exists public.submissions (
  id uuid primary key default gen_random_uuid(),
  creator_name text not null check (char_length(creator_name) between 1 and 30),
  item_count integer not null check (item_count between 1 and 200),
  submitted_at timestamptz not null default now()
);

create table if not exists public.selection_items (
  id bigint generated by default as identity primary key,
  submission_id uuid not null references public.submissions(id) on delete cascade,
  sku text not null,
  product_name text not null,
  category text not null,
  style text,
  plan_level text,
  price numeric,
  image_url text,
  is_featured boolean not null default false,
  intent text not null default '直播挂车',
  remark text not null default '',
  created_at timestamptz not null default now()
);

alter table public.selection_items
  add column if not exists style text,
  add column if not exists plan_level text,
  add column if not exists price numeric,
  add column if not exists image_url text,
  add column if not exists is_featured boolean not null default false,
  add column if not exists intent text not null default '鐩存挱鎸傝溅',
  add column if not exists remark text not null default '',
  add column if not exists created_at timestamptz not null default now();

create table if not exists public.product_overrides (
  sku text primary key,
  price numeric,
  image_url text,
  plan_level text,
  style text,
  updated_by text,
  updated_at timestamptz not null default now()
);

alter table public.product_overrides
  add column if not exists price numeric,
  add column if not exists image_url text,
  add column if not exists plan_level text,
  add column if not exists style text,
  add column if not exists updated_by text,
  add column if not exists updated_at timestamptz not null default now();

create index if not exists selection_items_submission_id_idx
  on public.selection_items(submission_id);
create index if not exists selection_items_sku_idx
  on public.selection_items(sku);
create index if not exists submissions_submitted_at_idx
  on public.submissions(submitted_at desc);
create index if not exists product_overrides_updated_at_idx
  on public.product_overrides(updated_at desc);

